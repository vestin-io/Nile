const MESSAGE_CATALOG: Record<LanguagePreference, Messages> = {
  en: EN_MESSAGES,
  zh: ZH_MESSAGES,
  ko: KO_MESSAGES,
  ja: JA_MESSAGES,
  th: TH_MESSAGES,
  fr: FR_MESSAGES,
  es: ES_MESSAGES,
  it: IT_MESSAGES,
  de: DE_MESSAGES,
  vi: VI_MESSAGES,
};

export type Translator = (key: string, variables?: Record<string, string | number>) => string;

export function createTranslator(language: LanguagePreference): Translator {
  const messages = MESSAGE_CATALOG[language];
  return (key, variables) => {
    const template = messages[key] ?? MESSAGE_CATALOG.en[key] ?? key;
    if (!variables) {
      return template;
    }

    return Object.entries(variables).reduce(
      (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
      template,
    );
  };
}

export function themeLabelKey(theme: ThemePreference): string {
  return `settings.theme.${theme}`;
}
